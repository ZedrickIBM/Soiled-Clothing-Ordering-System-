<h1>About Soiled</h1>
<p>Soiled is a clothing brand established in 2021 during the surge of pandemic. In the span of four years, Soiled has produced a variety of products ranging from shirts to tube masks and pomade. With a growing number of clothing brands blooming in the market, Soiled aims of becoming one that delivers quality and significant impact to the industry.
</p>
<h2>Mission</h2>
<ul>
    <li>Design and create high-quality clothing that empowers individuals to express their unique style. </li>
    <li>Embrace the unconventional by offering unexpected and innovative designs.</li>
    <li>Foster a sense of community by connecting with our customers and supporting local artists/causes. </li>
    <li>Maintain a commitment to our principles in all aspects of our business.</li>
</ul>
<h2>Vision</h2>
<p>To redefine the meaning of "Soiled" and transform it into a symbol of  modern  fashion. We envision a world where self-expression is embraced through clothing that tells a story, pushing the boundaries of conventional style.</p>

